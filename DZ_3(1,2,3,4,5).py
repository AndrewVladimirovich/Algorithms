# Запишем в переменную num самый встречаемый элемент массива, а в переменную max_frequency количество раз которое он встречается.
# По очереди берем элементы массива и сравниваем с элементами стоящими после него.
# Если значения совпадают то увеличиваем значение frequency на 1
# После того как посчитано количество раз какое встречается элемент переменная frequency сравнивается с max_frequency и
# если frequency больше то значения num  и max_frequency перезаписываются.
n = 15
arr = [0] * n
for i in range(n):
    arr[i] = int(random() * 20)
print(arr)
num = arr[0]
max_frequency = 1
for i in range(n - 1):
    frequency = 1
    for k in range(i + 1, n):
        if arr[i] == arr[k]:
            frequency = frequency + 1
    if frequency > max_frequency:
        max_frequency = frequency
        num = arr[i]
if max_frequency > 1:
    print(max_frequency, 'раз(а) встречается это число', num)
else:
    print('Числа не повторяются')


