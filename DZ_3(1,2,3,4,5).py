# Перебираем массив в цикле. Если очередной элемент меньше нуля и значение переменной равно -1, то это первый отрицательный
# элемент который нам встретился. Если это очередной отрицательный элемент то сравниваем значение текущего элемента с
# тем поторый содержится по индексу переменной.
# Если он больше то записываем его в индекс переменной.
# После завершения цикла проверяем равно ли значение переменой -1 если нет то выводим индекс максимального
# отрцательного элемента в массиве.
n = 5
arr = []
for i in range(n):
    arr.append(int(random() * 100) - 50)
print(arr)
i = 0
index = -1
while i < n:
    if arr[i] < 0 and index == -1:
        index = i
    elif arr[i] < 0 and arr[i] > arr[index]:
        index = i
    i = i + 1

print(index + 1, ' : ', arr[index])

